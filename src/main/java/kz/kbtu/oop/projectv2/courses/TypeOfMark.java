package kz.kbtu.oop.projectv2.courses;

/**
 * Перечисление, представляющее типы оценок в рамках курса.
 * Включает в себя типы оценок: FIRST_ATTESTATION, SECOND_ATTESTATION, FINAL.
 */
public enum TypeOfMark {
    /**
     * Первая аттестация.
     */
    FIRST_ATTESTATION,

    /**
     * Вторая аттестация.
     */
    SECOND_ATTESTATION,

    /**
     * Оценка за сессию.
     */
    FINAL
}
